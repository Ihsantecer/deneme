package ders04_matematikselişlemler;

public class C05_IncrementDecrement {
    public static void main(String[] args) {

        int sayi= 10;
        // bu sayıyı 3 artıralım ve yazdıralım
        sayi=sayi+3;
        sayi += 3;
        int s= 10;
        s= s+1;//11
        s+=1;//12
        s++;//13
        ++s;//14
        s++;
        s *= 3;

        System.out.println(s);
    }





}
